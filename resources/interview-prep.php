    <!-- Add interactive checklist functionality -->
    <script>
        // Save checklist state to localStorage
        document.addEventListener('DOMContentLoaded', function() {
            // Load saved checkbox states
            const checkboxes = document.querySelectorAll('.checklist-section input[type="checkbox"]');
            
            checkboxes.forEach((checkbox, index) => {
                // Set checkbox state from localStorage if available
                const isChecked = localStorage.getItem(`interview-prep-checkbox-${index}`) === 'true';
                checkbox.checked = isChecked;
                
                // Add event listener to save state
                checkbox.addEventListener('change', function() {
                    localStorage.setItem(`interview-prep-checkbox-${index}`, this.checked);
                });
            });
            
            // Add reset button functionality
            const resetButton = document.createElement('button');
            resetButton.className = 'btn btn-secondary mt-4 mb-5';
            resetButton.textContent = 'Reset Checklist Progress';
            resetButton.addEventListener('click', function() {
                checkboxes.forEach((checkbox, index) => {
                    checkbox.checked = false;
                    localStorage.setItem(`interview-prep-checkbox-${index}`, 'false');
                });
            });
            
            // Add reset button before the download box
            const downloadBox = document.querySelector('.resource-download-box');
            downloadBox.parentNode.insertBefore(resetButton, downloadBox);
        });
    </script>
