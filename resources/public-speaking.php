                <h3>Public Speaking Misconceptions That Hold Students Back</h3>
                
                <div class="case-section mb-5 p-4 bg-light rounded">
                    <p>Many students have encountered conventional advice about public speaking that can actually interfere with their development:</p>
                    
                    <div class="accordion" id="accordionMisconceptions">
                        <div class="accordion-item">
                            <h2 class="accordion-header" id="headingOne">
                                <button class="accordion-button" type="button" data-bs-toggle="collapse" data-bs-target="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
                                    <strong>Misconception #1: "Just imagine the audience in their underwear"</strong>
                                </button>
                            </h2>
                            <div id="collapseOne" class="accordion-collapse collapse show" aria-labelledby="headingOne" data-bs-parent="#accordionMisconceptions">
                                <div class="accordion-body">
                                    <p><strong>Why this misconception persists:</strong> This often-repeated advice attempts to address status anxiety by artificially lowering the perceived status of audience members.</p>
                                    <p><strong>The reality:</strong> This approach typically backfires because it (1) creates distracting mental imagery, (2) fails to address the underlying physiological arousal, and (3) positions the speaker against the audience rather than connecting with them.</p>
                                    <p><strong>The core problem:</strong> Effective speaking requires genuine connection with the audience, not psychological tricks that create artificial distance. Speakers who view their audience as partners rather than threats develop more authentic and impactful communication styles.</p>
                                </div>
                            </div>
                        </div>
                        
                        <div class="accordion-item">
                            <h2 class="accordion-header" id="headingTwo">
                                <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
                                    <strong>Misconception #2: "Great speakers are naturally charismatic"</strong>
                                </button>
                            </h2>
                            <div id="collapseTwo" class="accordion-collapse collapse" aria-labelledby="headingTwo" data-bs-parent="#accordionMisconceptions">
                                <div class="accordion-body">
                                    <p><strong>Why this misconception persists:</strong> We typically see only the polished final product of experienced speakers, not the years of practice, feedback, and incremental improvement that preceded it.</p>
                                    <p><strong>The reality:</strong> Research consistently shows that effective public speaking is a learned skill developed through deliberate practice rather than an innate personality trait. Many renowned speakers, including Warren Buffett, were once terrified of public speaking and deliberately worked to develop this capability.</p>
                                    <p><strong>The core problem:</strong> Students who believe speaking ability is innate often avoid opportunities for growth, create self-fulfilling prophecies about their abilities, and miss the chance to develop this crucial professional skill.</p>
                                </div>
                            </div>
                        </div>
                        
                        <div class="accordion-item">
                            <h2 class="accordion-header" id="headingThree">
                                <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
                                    <strong>Misconception #3: "Memorize your speech word-for-word"</strong>
                                </button>
                            </h2>
                            <div id="collapseThree" class="accordion-collapse collapse" aria-labelledby="headingThree" data-bs-parent="#accordionMisconceptions">
                                <div class="accordion-body">
                                    <p><strong>Why this misconception persists:</strong> Memorization creates an illusion of control and preparation that feels reassuring to anxious speakers.</p>
                                    <p><strong>The reality:</strong> Verbatim memorization typically leads to more wooden delivery, increases anxiety about forgetting lines, and prevents authentic connection with the audience. When a memorized speaker loses their place, they often experience catastrophic recovery problems.</p>
                                    <p><strong>The core problem:</strong> Effective speaking requires flexibility and adaptability to audience reactions, which becomes nearly impossible when rigidly adhering to a memorized script. The cognitive load of verbatim recall also prevents speakers from being fully present.</p>
                                </div>
                            </div>
                        </div>
                        
                        <div class="accordion-item">
                            <h2 class="accordion-header" id="headingFour">
                                <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseFour" aria-expanded="false" aria-controls="collapseFour">
                                    <strong>Misconception #4: "Great speaking is about performance techniques"</strong>
                                </button>
                            </h2>
                            <div id="collapseFour" class="accordion-collapse collapse" aria-labelledby="headingFour" data-bs-parent="#accordionMisconceptions">
                                <div class="accordion-body">
                                    <p><strong>Why this misconception persists:</strong> Technical advice about gestures, voice modulation, and movement is concrete and easier to teach than the deeper aspects of effective communication.</p>
                                    <p><strong>The reality:</strong> While delivery techniques matter, research shows that audiences primarily respond to perceived authenticity, clarity of message, and the speaker's connection to their material. Speakers who focus exclusively on performance elements often come across as inauthentic or manipulative.</p>
                                    <p><strong>The core problem:</strong> Students often focus on superficial aspects of delivery while neglecting the more fundamental elements of compelling communication: clear structure, genuine conviction, and audience-centered messaging.</p>
                                </div>
                            </div>
                        </div>
