                                <h4 class="card-title"><i class="fas fa-project-diagram text-primary me-2"></i> High-Stakes Context</h4>
                                <p class="card-text">Scenarios often involve ambiguity, conflicting priorities, tight deadlines, and high-visibility situations where millions of dollars may be at stake.</p>
                            </div>
