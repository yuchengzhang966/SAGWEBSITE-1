                        <div id="collapseOne" class="accordion-collapse collapse show" aria-labelledby="headingOne" data-bs-parent="#accordionQuestions">
                            <div class="accordion-body">
                                <p><strong>Why it's challenging:</strong> Consulting requires influencing clients and stakeholders without formal authority. This question probes your ability to persuade through expertise, relationships, and communication skills.</p>
                                
                                <p><strong>Key elements for success:</strong></p>
                                <ul>
                                    <li>Focus on a specific situation with meaningful stakes</li>
                                    <li>Demonstrate that you took time to understand the other person's perspective and priorities</li>
                                    <li>Explain your strategic approach to influence (data, relationship, coalition-building, etc.)</li>
                                    <li>Highlight how you communicated effectively and adjusted your approach if needed</li>
                                    <li>Share the positive outcome and what you learned about influence</li>
                                </ul>
                            </div>
