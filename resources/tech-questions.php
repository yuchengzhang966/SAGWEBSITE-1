                        <div id="collapseTwo" class="accordion-collapse collapse" aria-labelledby="headingTwo" data-bs-parent="#accordionQuestions">
                            <div class="accordion-body">
                                <p><strong>Why it's challenging:</strong> Consultants must regularly deliver unwelcome messages to clients and team members. This question tests your diplomacy, courage, and emotional intelligence.</p>
                                
                                <p><strong>Key elements for success:</strong></p>
                                <ul>
                                    <li>Choose a specific situation where the feedback was necessary but difficult</li>
                                    <li>Explain your preparation and thought process before the conversation</li>
                                    <li>Detail your approach - how you created the right setting and structured the conversation</li>
                                    <li>Demonstrate balance between directness and sensitivity</li>
                                    <li>Share how the recipient responded and the ultimate outcome</li>
                                    <li>Reflect on what you learned about effective feedback delivery</li>
                                </ul>
                            </div>
                        </div>
                    </div>
                    
