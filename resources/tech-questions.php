                        <div id="collapseThree" class="accordion-collapse collapse" aria-labelledby="headingThree" data-bs-parent="#accordionQuestions">
                            <div class="accordion-body">
                                <p><strong>Why it's challenging:</strong> Consulting often involves making recommendations with imperfect information under time constraints. This question assesses your comfort with ambiguity and decision-making process.</p>
                                
                                <p><strong>Key elements for success:</strong></p>
                                <ul>
                                    <li>Select a situation with meaningful consequences and genuine uncertainty</li>
                                    <li>Explain what information you had and what was missing</li>
                                    <li>Articulate your structured approach to decision-making despite the gaps</li>
                                    <li>Describe how you managed and communicated the inherent risks</li>
                                    <li>Share the outcome and whether additional information later validated or challenged your decision</li>
                                    <li>Reflect on how this experience shaped your approach to decisions under uncertainty</li>
                                </ul>
                            </div>
