                                <p><strong>Key elements for success:</strong></p>
                                <ul>
                                    <li>Select a situation involving substantial, unexpected change</li>
                                    <li>Describe your initial reaction honestly, while emphasizing your quick pivot to solution mode</li>
                                    <li>Explain the specific actions you took to adapt successfully</li>
                                    <li>Highlight any leadership you showed in helping others adjust</li>
                                    <li>Share the positive outcome that resulted from your adaptability</li>
                                    <li>Reflect on how this experience prepared you for future changes</li>
